/**
 * Дата, отформатированная в вид "07-05-2020",
 * где 07 - номер месяца (отсчёт с единицы), 05 - день, 2020 - год
 */
export const USA_MINUS_DATE_FORMAT = 'MM-DD-YYYY';

/**
 * Локализируемый формат даты в виде "09/04/1986" – для английской локали,
 * 04.09.1986 – для русской локали
 */
export const LOCAL_EXTENDED_DATE_FORMAT = 'L';

/**
 * Время, отформитрованное в виде "07:03" (часы, затем минуты)
 */
export const HOURS_MINUTES_EXTENDED_FORMAT = 'HH:mm';

/**
 * Дата, отформатированная в виде "декабрь 2022"
 */
export const FULL_MONTH_YEAR_FORMAT = 'MMMM Y';

/**
 * Дата, отформатированная в виде "пн", "вт" и т.д (день недели)
 */
export const SHORTEST_WEEKDAY_FORMAT = 'dd';
